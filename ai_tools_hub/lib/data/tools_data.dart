    case ToolCategory.image:        return const Color(0xFFA855F7);
    case ToolCategory.video:        return const Color(0xFFEC4899);
    case ToolCategory.voice:        return const Color(0xFF06B6D4);
    case ToolCategory.coding:       return const Color(0xFF10B981);
    case ToolCategory.education:    return const Color(0xFFF59E0B);
    case ToolCategory.marketing:    return const Color(0xFFEF4444);
    case ToolCategory.productivity: return const Color(0xFF3B82F6);
    case ToolCategory.translation:  return const Color(0xFF14B8A6);
    case ToolCategory.design:       return const Color(0xFFF97316);
    case ToolCategory.dataAnalysis: return const Color(0xFF0EA5E9);
    case ToolCategory.chat:         return const Color(0xFF84CC16);
  }
}

